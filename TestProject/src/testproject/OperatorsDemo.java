package testproject;

public class OperatorsDemo {

	public static void main(String[] args) {
		//post increment 
		int a = 20;
		a = a++;  
		System.out.println(a);
		//pre increment
		int b = 20;
		b = ++b;
		System.out.println(b);
		//post decrement
		int c = 20;
		c = c--;
		System.out.println(c);
		//pre decrement
		int d = 20;
		d = --d;
		System.out.println(d);
	}
} 
